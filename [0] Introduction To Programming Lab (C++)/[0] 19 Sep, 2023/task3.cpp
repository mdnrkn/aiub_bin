/*
Problem Statement :
Write a c++ program to print the following pattern using "cout" only -

*
**
***
****
*****
******

*/

#include <iostream>
using namespace std;
int main()
{
    cout << "*" << endl
         << "**" << endl
         << "***" << endl
         << "****" << endl
         << "*****" << endl
         << "******" << endl;
    return 0;
}

/*
Solution approach :
1. According to the problem statement, there is no need to use conditions.
2. Simply print the pattern using asterisk symbol.
*/